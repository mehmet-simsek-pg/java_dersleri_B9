package gun1;

public class EscapeCharacters {
    public static void main(String[] args) {

        /**
         * String(yazilar) icin kullanicagimiz özel karakterler.
         * \n -> yeni satir
         * \t -> tab boslugu olusturur
         * \" -> cift tirnak ekler
         * \' -> tek tirnak ekler
         * \\ -> ters slash ten bir tane ekler
         * */

        System.out.println("Satir1\nSatir2");
        System.out.println("Ad\tSoyad");
        System.out.println("O dedi ki: \"Merhaba Dünya\"");


    }
}
